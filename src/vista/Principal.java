    private void menuListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuListarActionPerformed
        // TODO add your handling code here:
        pnlAgregar.setVisible(false);
        pnlListar.setVisible(true);
        txtCodigoA.setEnabled(true);
        try {
            RegistroBiblioteca registro = new RegistroBiblioteca();
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(
                    new Object[]{"Codigo", "Nombre", "Formato",
                        "Paginas", "Categoria", "Autor", "Nuevo"});
            for (Libro tmp : registro.mostrar()) {
                modelo.addRow(
                        new Object[]{tmp.getCodigo(),
                            tmp.getNombre(),
                            tmp.getFormato(),
                            tmp.getPaginas(),
                            tmp.getCategoria(),
                            tmp.getAutor(),
                            tmp.isNuevo()
                        });

            }
            tbListar.setModel(modelo);
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Problemas con el driver de conecion");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Problema sql " + ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_menuListarActionPerformed

