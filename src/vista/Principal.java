    private void btnGuardarAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarAActionPerformed
        // TODO add your handling code here:
        try {
            RegistroBiblioteca registro = new RegistroBiblioteca();
            Libro libro = new Libro(txtCodigoA.getText(),
                    txtNombreA.getText(),
                    rbtnBlanda.isSelected() ? "D" : "B",
                    Integer.parseInt(txtCantidadA.getText()),
                    cbxCategoriaA.getSelectedItem().toString(),
                    txtAutorA.getText(),
                    chxNuevo.isSelected());

            if (registro.agregar(libro) == 1) {
                JOptionPane.showMessageDialog(this, "Se agrego");
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "No agrego");
            }
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Problemas con el driver de conecion");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Problema sql " + ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);

        }

    }//GEN-LAST:event_btnGuardarAActionPerformed

