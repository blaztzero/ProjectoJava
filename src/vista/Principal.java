    private void btnModificarLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarLActionPerformed
        // TODO add your handling code here:
       try {
            RegistroBiblioteca registro = new RegistroBiblioteca();
            String busqueda = txtBuscarL.getText();

            if (registro.buscar(busqueda) != null) {
                JOptionPane.showMessageDialog(this, " se encontro : \n" + registro.buscar(busqueda).toString());

                txtCodigoA.setText(registro.buscar(busqueda).getCodigo());
                txtNombreA.setText(registro.buscar(busqueda).getNombre());
                txtCantidadA.setText(registro.buscar(busqueda).getPaginas() + "");
                if (registro.buscar(busqueda).getFormato().equals("B")) {
                    rbtnBlanda.setSelected(true);
                } else {
                    rbtnDuraA.setSelected(true);
                }

                cbxCategoriaA.setSelectedItem(registro.buscar(busqueda).getCategoria());
                txtAutorA.setText(registro.buscar(busqueda).getAutor());

                chxNuevo.setSelected(registro.buscar(busqueda).isNuevo());
                txtCodigoA.setEnabled(false);
                pnlListar.setVisible(false);
                pnlAgregar.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "No se encontro");
            }
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Problemas con el driver de conecion");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Problema sql " + ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_btnModificarLActionPerformed

    private void btnBuscarLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarLActionPerformed
        // TODO add your handling code here:
        try {
            RegistroBiblioteca registro = new RegistroBiblioteca();
            String busqueda = txtBuscarL.getText();

            if (registro.buscar(busqueda) != null) {
                JOptionPane.showMessageDialog(this, "se encontro");

                DefaultTableModel modelo = new DefaultTableModel();
                modelo.setColumnIdentifiers(
                        new Object[]{"Codigo", "Nombre", "Formato",
                            "Paginas", "Categoria", "Autor", "Nuevo"});
                modelo.addRow( 
                        new Object[]{registro.buscar(busqueda).getCodigo(),
                            registro.buscar(busqueda).getNombre(),
                            registro.buscar(busqueda).getFormato(),
                            registro.buscar(busqueda).getPaginas(),
                            registro.buscar(busqueda).getCategoria(),
                            registro.buscar(busqueda).getAutor(),
                            registro.buscar(busqueda).isNuevo(),
                            });
                tbListar.setModel(modelo);
            } else {
                JOptionPane.showMessageDialog(this, "No Encontro Nada :(");
            }
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Problemas con el driver de conecion");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Problema sql " + ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
        
    }//GEN-LAST:event_btnBuscarLActionPerformed

